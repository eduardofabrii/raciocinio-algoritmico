# Convertendo temperatura
print('='*60)
print('Vamos converter a temperatura em CELSIUS para FAHRENHEIT')
temperatura_celsius = float(input('Indique a temperatura em C°: '))
temperatura_fahrenheit = (temperatura_celsius * 1.8) + 32
print(f'A temperatura {temperatura_celsius}° é equivalente a {temperatura_fahrenheit}°F')
